package com.qa.assessment;

public class Player {
	double xPosition;
	double yPosition;
	String name;

	public double getXPosition() {
		return xPosition;
	}

	public void setXPosition(double x) {
		this.xPosition = x;
	}

	public double getYPosition() {
		return yPosition;
	}

	public void setYPosition(double y) {
		this.yPosition = y;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
